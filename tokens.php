<h5 class="tw_bullet">C Tokens:</h5>
<font color="BLACK"><p>
 C tokens, Identifiers and Keywords are explained in this page one by one with simple examples.<br>

  <br>  * C Tokens : These are the basic buildings blocks in C language which are constructed together to write a C program.
  <br>  * Each and every smallest individual units in a C program are known as C Tokens.
   <br> * C Tokens are of six types.
<br>
<br>1) Keywords              		(eg: int, while),
<br>2) Identifiers               	(eg: main, total),
<br>3) Constants              		(eg: 10, 20),
<br>4) Strings                   	(eg: “total”, “hello”),
<br>5) Special symbols  		(eg: (), {}),
<br>6) Operators              		(eg: +, /,-,*)
</font></p>

<br><br><h4><font color="red">C Tokens example program:</font></h4>

<p><font color="purple"><b>
int main()<br>
{<br>
    int x, y, total;<br>
    x = 10, y = 20;<br>
    total = x + y;<br>
    Printf (“Total = %d \n”, total);<br>
}</b><br>
</font>
</p>
<br>
<p><font color="BLACK">where, 

              <br>  + main – identifier
               <br> + {,}, (,) – delimiter
               <br> + int – keyword
               <br> + x, y, total – identifier
               <br> + main, {, }, (, ), int, x, y, total – tokens

</font>
</p>

